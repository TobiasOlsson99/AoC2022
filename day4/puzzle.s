            # RAX: Return value, integer
    MOVQ $0, %RAX
    MOV $0, %RBX
readloop:
    MOV (%RSI), %BL # EBX <- Value at RSI
    CMP $'0', %BL # If ascii code less than '0'
    JL readend
    CMP $'9', %BL # If ascii code more than '9'
    JG readend
    MOV $10, %EDX
    MUL %EDX # multiply current integer by 10
    SUBQ $'0', %RBX # ASCII to integer
    ADDQ %RBX, %RAX 
    INCQ %RSI
    JMP readloop

